#! /usr/bin/env ruby
# coding: utf-8

require 'pp'
$y = 9
$x = 11
$input = [['w', '.', '.', '.', '.', '.', '.', '.', '.', 'w', 'w', '.'],
         ['.', 'w', 'w', 'w', '.', '.', '.', '.', '.', 'w', 'w', 'w'],
         ['.', '.', '.', '.', 'w', 'w', '.', '.', '.', 'w', 'w', '.'],
         ['.', '.', '.', '.', '.', '.', '.', '.', '.', 'w', 'w', '.'],
         ['.', '.', '.', '.', '.', '.', '.', '.', '.', 'w', '.', '.'],
         ['.', '.', 'w', '.', '.', '.', '.', '.', '.', 'w', '.', '.'],
         ['.', 'w', '.', 'w', '.', '.', '.', '.', '.', 'w', 'w', '.'],
         ['w', '.', 'w', '.', 'w', '.', '.', '.', '.', '.', 'w', '.'],
         ['.', 'w', '.', 'w', '.', '.', '.', '.', '.', '.', 'w', '.'],
         ['.', '.', 'w', '.', '.', '.', '.', '.', '.', '.', 'w', '.'],
        ]

def chase_W(y_pos, x_pos)
  $input[y_pos][x_pos] = '.'
  (-1..1).each do |y|
    (-1..1).each do |x|
      ny = y_pos + y
      nx = x_pos + x
      if (0 <= nx && nx <= $x && 0 <= ny && ny <= $y && $input[ny][nx] == 'w')
        chase_W(ny, nx)
      end
    end
  end
end

res = 0
(0..$y).each do |y|
  (0..$x).each do |x|
    if $input[y][x] == 'w'
      chase_W(y, x)
      res += 1
    end
  end
end

puts res
